import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export function showError(message: string) {
  toast.error(message, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: false,
    draggable: true,
    progress: undefined,
  });
}

